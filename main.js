function collectFiles(folder, files) {
  for (const child of folder.children) {
    if (child instanceof import_obsidian2.TFile) {
      files.push(child);
    } else if (child instanceof import_obsidian2.TFolder) {
      collectFiles(child, files);
    }
  }
}
