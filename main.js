// src/sync/operation-manager.ts
var OperationManager = class {
  constructor() {
    this.currentOperation = { type: "idle", startTime: 0 };
    /** 事件抑制类型（防止 vault 事件循环触发） */
    this.suppressEvent = "none";
  }
  /**
   * 获取当前操作类型
   */
  getCurrentType() {
    return this.currentOperation.type;
  }
  /**
   * 获取当前操作信息（用于显示）
   */
  getCurrentOperation() {
    const type = this.currentOperation.type;
    if (type === "idle") {
      return {
        type: "idle",
        displayName: "",
        isBlocking: false
      };
    }
    return {
      type,
      displayName: this.getDisplayName(type),
      isBlocking: this.isBlockingOperation(type),
      progress: this.currentOperation.progress
    };
  }
  /**
   * 检查是否有操作正在进行
   */
  isBusy() {
    return this.currentOperation.type !== "idle";
  }
  /**
   * 检查是否是阻塞型操作（禁止用户触发其他操作）
   */
  isBlocking() {
    return this.isBlockingOperation(this.currentOperation.type);
  }
  /**
   * 判断操作类型是否阻塞用户操作
   *
   * 阻塞型操作：bidirectional, pull, push
   * 非阻塞型操作：upload_batch, delete_batch, download_single（内部使用）
   */
  isBlockingOperation(type) {
    return type === "bidirectional" || type === "pull" || type === "push";
  }
  /**
   * 获取操作的显示名称
   */
  getDisplayName(type) {
    switch (type) {
      case "bidirectional":
        return t("menuSyncNow");
      case "pull":
        return t("menuPullFromRemote");
      case "push":
        return t("menuPushToRemote");
      case "upload_batch":
        return t("statusSyncing");
      case "delete_batch":
        return t("statusSyncing");
      case "download_single":
        return t("statusSyncing");
      default:
        return "";
    }
  }
  /**
   * 启动操作
   */
  start(type) {
    this.currentOperation = {
      type,
      startTime: Date.now()
    };
  }
  /**
   * 更新进度
   */
  updateProgress(current, total, phase) {
    if (this.currentOperation.type === "idle") {
      return;
    }
    this.currentOperation.progress = {
      current,
      total,
      phase
    };
  }
  /**
   * 结束操作
   */
  end() {
    this.currentOperation = { type: "idle", startTime: 0 };
    this.suppressEvent = "none";
  }
  /**
   * 检查是否可以启动新操作
   *
   * @param type 要启动的操作类型
   * @returns 是否可以启动
   */
  canStart(type) {
    if (this.currentOperation.type === "idle") {
      return true;
    }
    if (type === "download_single" && !this.isBlocking()) {
      return true;
    }
    return false;
  }
  // ========== 事件抑制方法（防止循环触发） ==========
  /**
   * 抑制 modify/create 事件
   * 用于下载文件时防止触发 handleFileChange
   */
  suppressModifyEvents() {
    this.suppressEvent = "modify";
  }
  /**
   * 抑制 delete 事件
   * 用于删除本地文件时防止触发 handleFileDelete
   */
  suppressDeleteEvents() {
    this.suppressEvent = "delete";
  }
  /**
   * 清除事件抑制
   */
  clearSuppress() {
    this.suppressEvent = "none";
  }
  /**
   * 检查是否应该抑制 modify/create 事件
   */
  shouldSuppressModify() {
    return this.suppressEvent === "modify";
  }
  /**
   * 检查是否应该抑制 delete 事件
   */
  shouldSuppressDelete() {
    return this.suppressEvent === "delete";
  }
};

